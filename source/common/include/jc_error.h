#ifndef JC_ERROR_H
#define JC_ERROR_H

#include "jc_types.h"

typedef jc_u16 jc_error_t;

enum {
  JC_E_OK = 0x0000
};

enum {
  JC_E_CATEGORY_BOOT_BASE = 0x0100,
  JC_E_CATEGORY_DISCOVERY_BASE = 0x0200,
  JC_E_CATEGORY_BDT_BASE = 0x0300,
  JC_E_CATEGORY_DEV_BASE = 0x0400,
  JC_E_CATEGORY_FS_BASE = 0x0500,
  JC_E_CATEGORY_EXEC_BASE = 0x0600,
  JC_E_CATEGORY_MODE_BASE = 0x0700,
  JC_E_CATEGORY_INTERNAL_BASE = 0x0F00
};

enum {
  JC_E_BOOT_BSP_MISSING = 0x0101,
  JC_E_BOOT_BSP_BAD_SIGNATURE = 0x0102,
  JC_E_BOOT_DISCOVERY_MISSING = 0x0103,
  JC_E_BOOT_UNSUPPORTED_PROFILE = 0x0104
};

enum {
  JC_E_DISCOVERY_BAD_SIGNATURE = 0x0201,
  JC_E_DISCOVERY_BAD_VERSION = 0x0202,
  JC_E_DISCOVERY_BAD_SIZE = 0x0203,
  JC_E_DISCOVERY_BAD_POINTER = 0x0204
};

enum {
  JC_E_BDT_BAD_SIGNATURE = 0x0301,
  JC_E_BDT_BAD_VERSION = 0x0302,
  JC_E_BDT_BAD_SIZE = 0x0303,
  JC_E_BDT_BAD_CHECKSUM = 0x0304
};

enum {
  JC_E_DEV_NOT_FOUND = 0x0401,
  JC_E_DEV_UNSUPPORTED = 0x0402,
  JC_E_DEV_IO_TIMEOUT = 0x0403,
  JC_E_DEV_IO_ERROR = 0x0404
};

enum {
  JC_E_FS_BAD_SUPER = 0x0501,
  JC_E_FS_BAD_DIR = 0x0502,
  JC_E_FS_NO_SPACE = 0x0503,
  JC_E_FS_NOT_FOUND = 0x0504
};

enum {
  JC_E_EXEC_BAD_MAGIC = 0x0601,
  JC_E_EXEC_BAD_VERSION = 0x0602,
  JC_E_EXEC_BAD_CRC = 0x0603,
  JC_E_EXEC_UNSUPPORTED_TIER = 0x0604
};

enum {
  JC_E_MODE_INVALID_TARGET = 0x0701,
  JC_E_MODE_NOT_PERMITTED = 0x0702,
  JC_E_MODE_STACK_OVERFLOW = 0x0703,
  JC_E_MODE_STACK_UNDERFLOW = 0x0704
};

enum {
  JC_E_INTERNAL_ASSERT = 0x0F01,
  JC_E_INTERNAL_UNREACHABLE = 0x0F02
};

#endif /* JC_ERROR_H */
